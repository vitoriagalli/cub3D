void	render_map(t_vars *vars)
{
	int i = 0;
	int j = 0;
	int x0;

	x0 = vars->point->x;
	while (i < row)
	{
		j = 0;
		vars->point->x = x0;
		while (j < column)
		{
			vars->point->color = map[i][j]> 0? 0xff000029 : 0x2ff9fca7;
			ft_rectangle(vars->data, *vars->point, tile_size, tile_size);
			vars->point->x = vars->point->x + tile_size;
			j++;
		}
		vars->point->y = vars->point->y + tile_size;
		i++;
	}
}
